def accelerate(func, log: bool = False):
    """
    A decorator to run operations on the GPU if Numba CUDA is available.
    """
    def wrapper(*args, **kwargs):
        if cuda.is_available():
            try:
                if log:
                    print("CUDA is available. Running on GPU.")
                # Allocate memory on the GPU
                d_args = [cuda.to_device(arg) if isinstance(arg, np.ndarray) else arg for arg in args]
                d_kwargs = {k: cuda.to_device(v) if isinstance(v, np.ndarray) else v for k, v in kwargs.items()}
                result = func(*d_args, **d_kwargs)
                # Copy result back to host
                if isinstance(result, cuda.devicearray.DeviceNDArray):
                    result = result.copy_to_host()
                return result
            except cuda.CudaSupportError:
                if log:
                    print("CUDA is not available. Running function on CPU.")
                return func(*args, **kwargs)
            except cuda.KernelRuntimeError:
                if log:
                    print("CUDA runtime error. Running function on CPU.")
                return func(*args, **kwargs)
            except:
                if log:
                    print("Unknown Error encountered. Running function on CPU.")
                return func(*args, **kwargs)
        else:
            if log:
                print("CUDA is not available. Running function on CPU.")
            return func(*args, **kwargs)
    return wrapper

class Neuron():
    def __init__(self, num_inputs: int, neuron_type: int = 0) -> None:
        self.num_in = num_inputs
        self.neuron_type = neuron_type
        self.weights = [np.random.randn() for i in range(num_inputs)]
        self.bias = np.random.randn()
        self.output = None
        self.inputs = None
    
    @accelerate
    def calculate(self, inputs: list[float], view_working: bool = False) -> float:
        self.inputs = np.array(inputs)
        weighted_sum = np.dot(self.inputs, self.weights) + self.bias
        self.output = self.activation(weighted_sum)
        if view_working:
            print(f"Inputs: {self.inputs}")
            print(f"Weights: {self.weights}")
            print(f"Bias: {self.bias}")
            print(f"Weighted Sum: {weighted_sum}")
            print(f"Activation Function: {'ReLU' if self.neuron_type == 0 else 'Sigmoid' if self.neuron_type == 1 else 'Tanh'}")
            print(f"Output: {self.output}")
            print()
        return self.output
    
    @accelerate
    def activation(self, x: float) -> float:
        if self.neuron_type == 0:
            return np.maximum(0, x)
        elif self.neuron_type == 1:
            return 1 / (1 + np.exp(-x))
        elif self.neuron_type == 2:
            return np.tanh(x)
        else:
            raise RuntimeError("No such type of neuron")
    
    @accelerate
    def activation_derivative(self, x: np.ndarray) -> np.ndarray:
        if self.neuron_type == 0:
            return np.where(x > 0, 1, 0)
        elif self.neuron_type == 1:
            s = 1 / (1 + np.exp(-x))
            return s * (1 - s)
        elif self.neuron_type == 2:
            return 1 - np.tanh(x)**2
        else:
            raise RuntimeError("No such type of neuron")
    
    @accelerate
    def calculate_gradient(self, error: float) -> tuple:
        delta = error * self.activation_derivative(self.output)
        weight_gradient = delta * self.inputs
        bias_gradient = delta
        max_grad = 1.0
        weight_gradient = np.clip(weight_gradient, -max_grad, max_grad)
        bias_gradient = np.clip(bias_gradient, -max_grad, max_grad)
        return weight_gradient, bias_gradient
    
    @accelerate
    def update_weights(self, weight_gradient: np.ndarray, bias_gradient: float, learning_rate: float) -> None:
        self.weights -= learning_rate * weight_gradient
        self.bias -= learning_rate * bias_gradient

