import re


class CheckClassName:
    _XIDStart = set()
    _XIDContinue = set()

    @classmethod
    def isXIDStart(cls, char):
        return ord(char) in cls._XIDStart

    @classmethod
    def isXIDContinue(cls, char):
        return ord(char) in cls._XIDContinue or cls.isXIDStart(char)

    @classmethod
    def sanitizeClassName(cls, name: str) -> str:
        if not isinstance(name, str) or not name:
            raise ValueError("Class name must be a non-empty string")

        if name[0] == "_":
            name = name[1:]

        # Replace invalid characters with underscores
        sanitized = []
        for i, char in enumerate(name):
            if (i == 0 and cls.isXIDStart(char)) or (i > 0 and cls.isXIDContinue(char)):
                sanitized.append(char)
            else:
                sanitized.append("_")

        sanitized_name = "".join(sanitized)

        # Ensure the first character is uppercase
        if sanitized_name and not sanitized_name[0].isupper():
            sanitized_name = "A" + sanitized_name[1:]

        return sanitized_name


_splitRegex = re.compile(r"([\dA-F]+)(?:\.\.([\dA-F]+))?\s*;?\s*")


def _getLettersFromData(data):
    letters = set()
    for line in data.splitlines():
        match = _splitRegex.match(line)
        if match:
            start = int(match.group(1), 16)
            end = int(match.group(2), 16) if match.group(2) else None
            if end is None:
                letters.add(start)
            else:
                for c in range(start, end + 1):
                    letters.add(c)

    return letters

